package sk.ivanmolcan.zviera;

public class Pes {
    private String name;
    private int legs;

    public Pes(String name, int legs) {
        this.name = name;
        this.legs = legs;
    }

    public String getName() {
        return name;
    }

    public int getLegs() {
        return legs;
    }
}
